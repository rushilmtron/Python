from numpy import *
arr1 = array([10,0.2,9,3,5])
arr2 = array([1,2,3,4,5])
print('Sine of array:', sin(arr1))
print('Cos of array:',cos(arr1))
print('Log of array:',log(arr1))
print('Sqrt of array:',sqrt(arr1))
print('Min of array:',min(arr1))
print('Max of array:',max(arr1))
print('unique of array:',unique(arr1))

print('Concatenate of array:',concatenate([arr1,arr2]))

